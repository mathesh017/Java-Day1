package Loops;
import java.util.*;
public class Loops {

	public static void main(String[] args) {
		int n=10;
		System.out.println("Ascending Order");
		for (int i=0;i<=n;i++) {
			System.out.println(i);
		}

		System.out.println("Descending Order");
		for (int i=1;n>=i;n--) {
			System.out.println(n);
		}
	}

}
